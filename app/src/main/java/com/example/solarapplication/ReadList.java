package com.example.solarapplication;

public class ReadList {
    double V;
    double I;

    public ReadList(double v, double i) {
        V = v;
        I = i;
    }

    public double getV() {
        return V;
    }

    public void setV(double v) {
        V = v;
    }

    public double getI() {
        return I;
    }

    public void setI(double i) {
        I = i;
    }
}
